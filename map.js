const map = function(array, callback) {
  const results = [];
  for (let item of array) {
    results.push(callback(item));
  }
  return results;
}

const words = ["up", "down", "left", "right"];

const results1 = map(words, (word) => "fly " + word);

assertArraysEqual(results1, ["fly up", "fly down", "fly left", "fly right"]);